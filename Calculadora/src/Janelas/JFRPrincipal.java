    public static boolean existePonto(String txt) {
        boolean rst;
        rst = false;

        for (int i = 0; i < txt.length(); i++) {
            if (txt.substring(i, i + 1).equals(".")) {
                rst = true;
                break;
            }
        }
        return rst;

    }
